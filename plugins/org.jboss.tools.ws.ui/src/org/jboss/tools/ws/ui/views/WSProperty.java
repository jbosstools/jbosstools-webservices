package org.jboss.tools.ws.ui.views;

/**
 * Represents Webservice request/response property
 * @since 2.0
 */
public class WSProperty {
	
	private String key;
	private String value;
	
	public WSProperty(String key, String value) {
		this.key = key;
		this.value = value;
	}
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		WSProperty other = (WSProperty) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}
	
	@Override
	public String toString() {
		if(key != null && value != null) {
			return key+"="+value; //$NON-NLS-1$
		} else if (key != null) {
			return key;
		}
		return value;
	}

}
